from sklearn.model_selection import StratifiedKFold
import tensorflow as tf
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense
from sklearn.metrics import accuracy_score, classification_report

# Define the number of splits for Stratified Cross-Validation
n_splits = 15

# Initialize Stratified K-Fold
stratified_kfold = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=42)

# Initialize lists to store accuracy scores and classification reports
accuracy_scores = []
classification_reports = []

# Perform Stratified Cross-Validation
for train_index, test_index in stratified_kfold.split(X_train_pca, y_train_resampled_df):
    X_train_fold, X_val_fold = X_train_pca[train_index], X_train_pca[test_index]
    y_train_fold, y_val_fold = y_train_resampled_df.iloc[train_index], y_train_resampled_df.iloc[test_index]

    # Define the model
    model = Sequential([
        Dense(512, activation='relu', input_shape=(X_train_pca.shape[1],)),
        Dense(256, activation='relu'),
        Dense(128, activation='relu'),
        Dense(64, activation='relu'),
        Dense(1, activation='sigmoid')
    ])

    # Compile the model
    model.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])

    # Train the model
    model.fit(X_train_fold, y_train_fold, epochs=25, batch_size=20, verbose=0)

    # Evaluate the model on validation data
    y_pred = (model.predict(X_val_fold) > 0.5).astype(int)

    # Calculate accuracy score
    accuracy = accuracy_score(y_val_fold, y_pred)
    accuracy_scores.append(accuracy)

    # Generate classification report
    classification_reports.append(classification_report(y_val_fold, y_pred))

# Print average accuracy score
print("Average Accuracy:", sum(accuracy_scores) / len(accuracy_scores))

# Print classification reports for each fold
for i, report in enumerate(classification_reports):
    print(f"Classification Report for Fold {i + 1}:")
    print(report)